      <i class="fa fa-quote-left"></i>
      <span><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">WIZ750SR（串口到以太网）有什么样的通信模式？</font></font></span>
      <a class="anchor" href="#faq_entry_4422"><i class="fa fa-link"></i></a>
     </div>
     <div class="faq-answer">
      <p><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">它基本上解释为串口到以太网。</font></font><br /><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">以太网通信模式具有串口转以太网的TCP Server / TCP Client / TCP混合/ UDP模式。</font></font></p> 
      <ul> 
       <li> <p><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">TCP服务器</font></font></p> 
        <ul> 
         <li><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">TCP服务器模式将产品设置为TCP服务器并等待来自客户端的连接。</font><font style="vertical-align: inherit;">（听模式）</font></font></li> 
         <li><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">如果你觉得容易，你可以认为你正在等待对方的电话。</font></font></li> 
        </ul> </li> 
       <li> <p><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">TCP客户端</font></font></p> 
        <ul> 
         <li><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">TCP客户端模式将产品设置为TCP客户端，并尝试使用要连接的TCP服务器的IP地址和端口号进行连接。</font></font></li> 
         <li><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">如果你轻易思考，那就是打电话给对方的行为。</font></font></li> 
        </ul> </li> 
       <li> <p><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">TCP混合</font></font></p> 
        <ul> 
         <li><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">TCP混合模式在TCP服务器/ TCP客户端模式下均可用。</font></font></li> 
         <li><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">当连接请求来自TCP客户端时，它充当TCP服务器。</font><font style="vertical-align: inherit;">如果有TCP服务器连接，它将充当TCP客户端并连接到TCP服务器。</font></font></li> 
        </ul> </li> 
       <li> <p><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">UDP</font></font></p> 
        <ul> 
         <li><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">UDP模式与TCP通信不同。</font><font style="vertical-align: inherit;">换句话说，它不同于协议的操作。</font></font></li> 
         <li><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">与TCP不同，它是一种不需要连接请求和连接概念的协议。</font></font></li> 
         <li><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">通过指定发送数据的IP和端口，UDP只需将相同的数据发送到所有物理连接的网络。</font></font></li> 
         <li><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">然后，所有物理连接的PC或产品都会收到数据并检查它们是否是自我数据。</font><font style="vertical-align: inherit;">如果他们的IP和PORT不同，他们不会收到数据。</font><font style="vertical-align: inherit;">如果IP和PORT匹配他们自己，他们会接受数据。</font></font></li> 
         <li><font style="vertical-align: inherit;"><font style="vertical-align: inherit;">也就是说，如果你向连接的每个人抛出不是连接概念的数据，那么每个人都在检查它们是否是他们的数据，如果是这样，拿走它们。</font></font></li> 
